L = float(input("Veuillez entrer la valeur de la longueur de votre rectangle :"))
l = float(input("Veuillez entrer la valeur de la largeur de votre rectangle :"))

perimetre = 2 * (L+l)
surface = (L*l)

print ("Le périmètre de votre rectangle est : " + str(perimetre) +"cm")
print ("La surface de votre rectangle est : " +str(surface) + "cm")

